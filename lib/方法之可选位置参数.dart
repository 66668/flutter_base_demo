/**
 * 学习（4）方法一可选位置参数
 *
 * 同java的重载，重载繁琐
 *
 * 设置默认值
 * fun([int i = 0, int j = 0, int k = 0, int m = 0]){}
 *
 * 参数为数组,用[]表示，默认可值赋值为0
 *
 * 如果给第2，第3个赋值，则前边参数必须赋值默认值，所以也比较麻烦，这就有了可选命名
 *
 */

void fun([int i = 0, int j = 0, int k = 0, int m = 0]) {
  print("${i}--${j}--${k}--${m}");
}

void main() {
  //i =1
  fun(1);

  // k = 3
  fun(0, 0, 1);

  //k= 4 m=5
  fun(0, 0, 4, 5);
}
